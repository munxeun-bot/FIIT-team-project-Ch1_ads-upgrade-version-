def generate_viewer_prompt(platform: str) -> str:
    guide = {
        "Instagram": """
[Instagram Reels 제작 가이드]
- 감정적인 훅으로 시작
- 분위기, 감성, 리듬을 중시
- 설명은 직접적이지 않게 자연스럽게 녹일 것
- 시청자가 '저장하고 싶게' 만드는 흐름
- CTA는 저장, 공감, 방문 유도 중심
- 감정 → 분위기 → 여운
- 정보는 간접적으로
- 가볍고 자연스러운 말투
- 리듬감 있는 문장, 친근한 표현 허용
- 말하듯 자연스러운 구어체
- 반말 또는 부드러운 존댓말 혼용 가능
- 문장 끝을 완전히 맺지 않아도 됨
- 감탄사, 여백, 여운 허용
- 감정과 분위기를 최우선
- 문장 짧게, 여백 있는 리듬
- 공감/느낌 위주 표현 
""",
        "YouTube": """
[YouTube Shorts 제작 가이드]
- 정보 또는 궁금증 훅으로 시작
- 무엇을 보여줄지 초반에 명확히 제시
- 설명 구조는 기-승-결이 분명해야 함
- 반복 시청과 다음 영상 연결을 고려
- CTA는 구독, 다음 영상 암시 중심
- 핵심 정보 → 이유 → 정리
- 짧고 명확한 정보 전달
- 핵심 행동 위주의 담백한 문장
- 존댓말만 사용
- 문장 끝을 분명히 마무리
- 누가 봐도 이해되는 표현
- 정보 전달과 이해를 최우선
- 설명형 문장, 명확한 구조
- 이유 → 결과 → 요약 흐름
- 불필요한 감성 표현 최소화
"""
    }

    platform_guide = guide.get(platform, "")

    return f"""
너는 카페 사장님을 위한 마케팅 전문가야.

플랫폼: {platform}
{platform_guide}

이 플랫폼에 맞는 릴스/쇼츠 콘텐츠를 볼 만한 주요 시청자를 고려해서 설명해줘.
아래 JSON 형식으로만 응답해줘.

예시:
{{
  "target": "10~20대 여성 직장인"
}}
"""